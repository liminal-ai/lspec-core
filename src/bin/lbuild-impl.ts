import { realpathSync } from "node:fs";
import { fileURLToPath } from "node:url";

import { defineCommand, runMain } from "citty";

import epicCleanupCommand from "../cli/commands/epic-cleanup";
import epicSynthesizeCommand from "../cli/commands/epic-synthesize";
import epicVerifyCommand from "../cli/commands/epic-verify";
import inspectCommand from "../cli/commands/inspect";
import preflightCommand from "../cli/commands/preflight";
import quickFixCommand from "../cli/commands/quick-fix";
import storyContinueCommand from "../cli/commands/story-continue";
import storyImplementCommand from "../cli/commands/story-implement";
import storySelfReviewCommand from "../cli/commands/story-self-review";
import storyVerifyCommand from "../cli/commands/story-verify";
import { packageVersion } from "../package-metadata";

function rootHelpText(): string {
	return `lbuild-impl v${packageVersion} - Build and verify stories from a spec pack

WHAT THIS DOES
  Agent chooses next step; CLI runs it and returns envelope. It reads spec pack,
  checks readiness, builds stories, resumes paused sessions, verifies work,
  applies bounded fixes, and records artifacts.

WHAT IT EXPECTS
  Spec pack generated by liminal-spec. Core files:
    - epic.md: what to build, with acceptance criteria and test conditions
    - tech-design.md: how to build and verify
    - stories/*.md: one bounded build unit per story

BASIC PROCESS
  1. inspect      Read the pack without changing it.
  2. preflight    Check config, git state, gates, and provider readiness.
  3. implement    Run story-implement for one story.
  4. continue     Use story-continue when an envelope returns a continuation handle.
  5. verify       Run story-self-review and story-verify.
  6. fix          Use quick-fix for bounded follow-up fixes.
  7. summarize    Use epic-verify and epic-synthesize to assess epic completion.

  Repeat steps 3-6 per story. Run cleanup only when batch prepared.

USAGE
  lbuild-impl <command> [options]
  lbuild-impl <command> --help

CORE OPTIONS
  Most commands use:
    --spec-pack-root <path>   Path to the spec-pack directory.
    --json                   Print a parseable JSON envelope.

  Story commands need --story-id <story-id>. Run command help before mutating commands.

COMMANDS
  inspect             Read-only spec-pack inspection. Best first command.
  preflight           Validate config, gates, git state, and provider readiness.

  story-implement     Start implementation for one story.
  story-continue      Continue a retained story implementation session.
  story-self-review   Review retained story session's work.
  story-verify        Start or continue story verification.

  quick-fix           Apply one bounded fix from task description.
  epic-verify         Run epic-level verification.
  epic-synthesize     Summarize/reconcile epic verifier findings.
  epic-cleanup        Apply prepared cleanup batch.

OUTPUT CONTRACT
  Use --json for agent automation. Every command returns an envelope.

  Check:
    status      Did the CLI/runtime succeed?
    outcome     What happened operationally: ready, completed, blocked, etc.
    errors      What needs attention if status/outcome is not successful.
    artifacts   Evidence files written under the spec pack.
    continuationHandle   Resume token when long-running work can continue.

  Do not treat status=ok as proof the requested implementation is complete.
  Read outcome, errors/warnings, artifacts, and continuationHandle.

  If outcome is blocked, read errors/warnings/artifacts, fix prerequisites or
  report the blocker, then rerun the appropriate command.

SAFETY
  - inspect is read-only.
  - preflight checks readiness before implementation work.
  - implementation, verification, quick-fix, and cleanup commands may mutate the
    target codebase and write artifacts into the spec pack.
  - Expect a dirty git worktree after mutating commands.
  - Provider-backed commands may call paid/authenticated external CLIs.
  - Current provider binaries: claude, codex, copilot.

EXAMPLES
  lbuild-impl inspect --spec-pack-root ./docs/spec-build/epics/my-epic --json
  lbuild-impl preflight --spec-pack-root ./docs/spec-build/epics/my-epic --json
  lbuild-impl story-implement --spec-pack-root ./docs/spec-build/epics/my-epic --story-id 00-foundation --json

SUGGESTED AGENT START
  1. Run inspect --json.
  2. Read status, outcome, warnings, and artifacts.
  3. Run preflight --json.
  4. Choose the next command from the process above.
  5. Run lbuild-impl <command> --help before each new mutating command.

MORE HELP
  lbuild-impl <command> --help`;
}

const main = defineCommand({
	meta: {
		name: "lbuild-impl",
		version: packageVersion,
		description:
			"Implementation runtime for Liminal Build — agentic impl/verify orchestration.",
	},
	default:
		"Use `lbuild-impl inspect --spec-pack-root <path> --json` to validate a spec pack.",
	subCommands: {
		"epic-cleanup": epicCleanupCommand,
		"epic-synthesize": epicSynthesizeCommand,
		"epic-verify": epicVerifyCommand,
		inspect: inspectCommand,
		preflight: preflightCommand,
		"quick-fix": quickFixCommand,
		"story-implement": storyImplementCommand,
		"story-continue": storyContinueCommand,
		"story-self-review": storySelfReviewCommand,
		"story-verify": storyVerifyCommand,
	},
});

function isRootHelpRequest(argv: string[]): boolean {
	return (
		argv.length === 0 ||
		(argv.length === 1 && ["-h", "--help"].includes(argv[0]))
	);
}

function isMainModule() {
	if (!process.argv[1]) {
		return false;
	}

	try {
		return fileURLToPath(import.meta.url) === realpathSync(process.argv[1]);
	} catch {
		return fileURLToPath(import.meta.url) === process.argv[1];
	}
}

if (isMainModule()) {
	if (isRootHelpRequest(process.argv.slice(2))) {
		console.log(rootHelpText());
		process.exit(0);
	}

	runMain(main).catch((error: unknown) => {
		console.error(error instanceof Error ? error.message : String(error));
		process.exit(1);
	});
}

export default main;
